/**
 * The Player enum represents the participants in the Reversi game,
 * including X (Black), O (White), and EMPTY.
 * Each player, X and O, signifies a colored disc on the Reversi board,
 * while EMPTY represents an unoccupied space (a disc does not exist there yet).
 */

public enum PlayerPiece {
  // black, white
  X, O, EMPTY;
}